public class Main {
    public static void main(String[] args) {


        System.out.println("\nCount to 4.");

        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }


        System.out.println("\nCount down to New Years!");

        int countdown = 3;

        while (countdown > 0) {
            System.out.println(countdown);
            countdown--;
        }

        System.out.println("Happy New Year!!");


/*
*       do / while example
*/
        System.out.println("\nDo/While");

        int a = 0;
        do {
            System.out.println(a);
            a++;
        }
        while (a < 5);




    }
}